// Valor padrão para evitar erro de provider
const defaultContextValue: TranslationContextType = {
  t: (key: string) => key,
  formatCurrency: (value: number) => `R$ ${value.toFixed(2)}`,
  formatNumber: (value: number) => value.toString(),
  formatDate: (date: Date | string) => new Date(date).toLocaleDateString("pt-BR"),
  getCurrencySymbol: () => "R$",
};

const TranslationContext = createContext<TranslationContextType>(defaultContextValue);

export const TranslationProvider: React.FC<{ children: React.ReactNode }> = ({
  children,
}) => {
  const t = (key: string, variables?: Record<string, any>): string => {
    const translation = translations[key as keyof typeof translations] || key;

    if (variables) {
      return Object.keys(variables).reduce((str, variable) => {
        return str.replace(
          new RegExp(`\\{${variable}\\}`, "g"),
          String(variables[variable])
        );
      }, translation);
    }

    return translation;
  };

  const formatCurrency = (value: number): string => {
    const options = {
      style: "currency" as const,
      currency: "BRL",
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    };

    // Formatação personalizada para o estilo brasileiro
    const formatted = new Intl.NumberFormat("pt-BR", options).format(value);
    return formatted;
  };

  const formatNumber = (value: number): string => {
    return new Intl.NumberFormat("pt-BR").format(value);
  };

  const formatDate = (date: Date | string): string => {
    const dateObj = typeof date === "string" ? new Date(date) : date;
    return new Intl.DateTimeFormat("pt-BR").format(dateObj);
  };

  const getCurrencySymbol = (): string => {
    return "R$";
  };

  return (
    <TranslationContext.Provider
      value={{
        t,
        formatCurrency,
        formatNumber,
        formatDate,
        getCurrencySymbol,
      }}
    >
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = (): TranslationContextType => {
  const context = useContext(TranslationContext);

  if (!context) {
    console.warn(
      "useTranslation: Using default context. Make sure TranslationProvider is wrapping your app.",
    );
    return defaultContextValue;
  }

  return context;
};
