// Valor padrão para evitar erro de provider
const defaultContextValue: TranslationContextType = {
  language: "pt-BR",
  currency: "BRL",
  setLanguage: () => {},
  setCurrency: () => {},
  t: (key: string) => key,
  formatCurrency: (value: number) => `R$ ${value.toFixed(2)}`,
  formatNumber: (value: number) => value.toString(),
  formatDate: (date: Date | string) => new Date(date).toLocaleDateString(),
  getCurrencySymbol: () => "R$",
  getAvailableLanguages: () => [
    { code: "pt-BR", name: "Português (BR)", flag: "🇧🇷" },
  ],
  getAvailableCurrencies: () => [
    { code: "BRL", name: "Real (BRL)", symbol: "R$" },
  ],
};

const TranslationContext =
  createContext<TranslationContextType>(defaultContextValue);

export const TranslationProvider: React.FC<{ children: React.ReactNode }> = ({
  children,
}) => {
  const [language, setLanguage] = useState<string>(() => {
    // Proteção para SSR - verificar se localStorage está disponível
    if (typeof window === "undefined") {
      return "pt-BR";
    }

    try {
      const saved = localStorage.getItem("language");
      if (saved && translations[saved as keyof typeof translations]) {
        return saved;
      }
    } catch (error) {
      console.warn("TranslationProvider: Error accessing localStorage:", error);
    }

    return "pt-BR";
  });

  const [currency, setCurrency] = useState<string>(() => {
    // Proteção para SSR - verificar se localStorage está disponível
    if (typeof window === "undefined") {
      return "BRL";
    }

    try {
      const saved = localStorage.getItem("currency");
      const validCurrencies = ["BRL", "USD", "EUR"];
      if (saved && validCurrencies.includes(saved)) {
        return saved;
      }
    } catch (error) {
      console.warn(
        "TranslationProvider: Error accessing localStorage for currency:",
        error,
      );
    }

    switch (language) {
      case "en-US":
        return "USD";
      case "es-ES":
        return "EUR";
      case "pt-BR":
      default:
        return "BRL";
    }
  });

  const t = (key: string, variables?: Record<string, any>): string => {
    const langTranslations =
      translations[language as keyof typeof translations];
    const translation =
      langTranslations?.[key as keyof typeof langTranslations] ||
      translations["pt-BR"][key as keyof (typeof translations)["pt-BR"]] ||
      key;

    if (variables) {
      return Object.keys(variables).reduce((str, variable) => {
        return str.replace(
          new RegExp(`{{${variable}}}`, "g"),
          String(variables[variable]),
        );
      }, translation);
    }

    return translation;
  };

  const formatCurrency = (value: number): string => {
    const options = {
      style: "currency" as const,
      currency: "BRL",
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    };

    // Formatação personalizada para o estilo brasileiro 
    const formatted = new Intl.NumberFormat("pt-BR", options).format(value);
    return formatted;
  };

  const formatNumber = (value: number): string => {
    return new Intl.NumberFormat(language).format(value);
  };

  const formatDate = (date: Date | string): string => {
    const dateObj = typeof date === "string" ? new Date(date) : date;
    return new Intl.DateTimeFormat(language).format(dateObj);
  };

  const getCurrencySymbol = (): string => {
    const symbols: Record<string, string> = {
      BRL: "R$",
      USD: "$",
      EUR: "€",
    };
    return symbols[currency] || "R$";
  };

  const getAvailableLanguages = () => [
    { code: "pt-BR", name: "Português", flag: "🇧🇷" },
    { code: "en-US", name: "Inglês", flag: "🇺🇸" },
    { code: "es-ES", name: "Espanhol", flag: "🇪🇸" },
  ];

  const getAvailableCurrencies = () => [
    { code: "BRL", name: "Real (BRL)", symbol: "R$" },
    { code: "USD", name: "Dollar (USD)", symbol: "$" },
    { code: "EUR", name: "Euro (EUR)", symbol: "€" },
  ];

  useEffect(() => {
    localStorage.setItem("language", language);
  }, [language]);

  useEffect(() => {
    localStorage.setItem("currency", currency);
  }, [currency]);

  return (
    <TranslationContext.Provider
      value={{
        language,
        currency,
        setLanguage,
        setCurrency,
        t,
        formatCurrency,
        formatNumber,
        formatDate,
        getCurrencySymbol,
        getAvailableLanguages,
        getAvailableCurrencies,
      }}
    >
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = (): TranslationContextType => {
  const context = useContext(TranslationContext);

  // Com o valor padrão, context nunca deve ser undefined
  // Mas vamos manter uma verificação defensiva
  if (!context) {
    console.warn(
      "useTranslation: Using default context. Make sure TranslationProvider is wrapping your app.",
    );
    return defaultContextValue;
  }

  return context;
};
